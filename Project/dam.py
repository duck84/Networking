class Dam():
    def __init__(self):
        self.MW = 0
        self.forbay = 0
        self.tailbay = 0
        self.spill = 0

    def get_MW(self):
        return self.MW

    def get_forbay(self):
        return self.forbay
    
    def get_tailbay(self):
        return self.tailbay

    def get_spill(self):
        return self.spill